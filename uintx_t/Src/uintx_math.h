/// \file uintx_math.h
/// \brief Declaration of some useful unsigned integer math functions.

#if !defined(__uintx_math__)
#define __uintx_math__

#include "uintx_t.h"

const uintx_t powerx(const uintx_t&, const uintx_t&); ///< Power.
const uintx_t sqrx(const uintx_t&); ///< Square.
const uintx_t factorialx(const uintx_t&); ///< Factorial.
const uintx_t gcdx(const uintx_t&, const uintx_t&); ///< Greatest common divisor.
const uintx_t fibx(const uintx_t&); ///< Fibonacci number.
const uintx_t sqrtx(const uintx_t&); ///< Square root.

#endif
